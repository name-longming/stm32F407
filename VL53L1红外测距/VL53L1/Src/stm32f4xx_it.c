/* USER CODE BEGIN 1 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	UNUSED(huart);
	
	int Distance_Data;
	
	if(huart == &huart6)
	{
		Uart6_RxBuffer[Uart6_Rx_Cnt++] = Uart6_aRxBuffer;
		Distance_Data = Get_Distance_VL53L1(Uart6_RxBuffer);
		if(Distance_Data != -1)
		{
			Uart6_Rx_Cnt=0;
			memset(Uart6_RxBuffer,0x00,sizeof(Uart6_RxBuffer)); //清空数组
		}
		HAL_UART_Receive_IT(&huart6, (uint8_t *)&Uart6_aRxBuffer, 1);
	}
}
